package Practices;

public class NaturalSum {

	public static void main(String[] args) {
		        int sum = 0;

		        // Calculate the sum of the first 50 natural numbers
		        for (int i = 1; i <= 50; i++) {
		            sum += i;
		        }
		        System.out.println("The sum of the first 50 natural numbers is " + sum + ".");
		    }	
	}
